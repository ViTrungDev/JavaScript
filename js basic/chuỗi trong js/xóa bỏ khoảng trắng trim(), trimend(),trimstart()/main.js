// xóa bỏ khảng trắng ở hai đầu
let s11 = "  he llo   work    ";
console.log(s11.length);
let s12 = s11.trim();
console.log(s12);
console.log(s12.length);
// trimEnd xóa bỏ khảng trắng cuối dãy
let s13 = "  he llo   work    ";
let s14 = s13.trimEnd();
console.log(s14);
// trimStart xóa khảng trắng đầu chuỗi
let s15 = "  he llo   work    ";
let s16 = s15.trimStart();
console.log(s16);
